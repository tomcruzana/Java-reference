//Short
String stringNumber = "100";
short Number = Short.parseShort(stringNumber);
System.out.println(Number + 321);  

//Int
String stringNumber = "100";
int Number = Integer.parseInt(stringNumber);
System.out.println(Number + 100); 

//Float
String stringNumber = "100";
float Number = Float.parseFloat(stringNumber);
System.out.println(Number + 100.3); 

//Double
String stringNumber = "100";
double Number = Double.parseDouble(stringNumber);
System.out.println(Number + 100.233); 

//Long
String stringNumber = "100";
long Number = Long.parseLong(stringNumber);
System.out.println(Number + 1002131.233); 